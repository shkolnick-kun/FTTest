#include "mutex.h"

bgrt_st_t bgrt_mtx_init_cs(bgrt_mtx_t * mutex, bgrt_prio_t prio)
{
    return BGRT_PRIV_SYNC_INIT(mutex, prio);
}

bgrt_st_t bgrt_mtx_init(bgrt_mtx_t * mutex, bgrt_prio_t prio)
{
    bgrt_st_t ret;
    BGRT_INT_LOCK();
    ret = bgrt_mtx_init_cs(mutex, prio);
    BGRT_INT_FREE();
    return ret;
}

bgrt_st_t bgrt_mtx_try_lock(bgrt_mtx_t * mutex)
{
    bgrt_st_t ret;

    if (!mutex)
    {
        return BGRT_ST_ENULL;
    }

    BGRT_PROC_LOCK();

    ret = BGRT_SYNC_OWN(mutex, 0);

    if (BGRT_ST_OK == ret)
    {
        BGRT_PROC_LOCK(); /*Now process must not stop!*/
    }

    BGRT_PROC_FREE();

    return ret;
}

bgrt_st_t bgrt_mtx_lock(bgrt_mtx_t * mutex)
{
    bgrt_st_t ret;

    if (!mutex)
    {
        return BGRT_ST_ENULL;
    }

    BGRT_PROC_LOCK(); /*Now process must not stop!*/

    ret = BGRT_SYNC_OWN(mutex, 1); /*Try to lock mutex*/

    if (BGRT_ST_EOWN == ret)
    {
        bgrt_flag_t touch = 1;
        ret = BGRT_SYNC_SLEEP(mutex, &touch);
    }

    return ret;
}

bgrt_st_t bgrt_mtx_free(bgrt_mtx_t * mutex)
{
    bgrt_st_t ret;

    if (!mutex)
    {
        return BGRT_ST_ENULL;
    }

    ret = BGRT_SYNC_WAKE(mutex,  BGRT_PID_NOTHING, 1);    /*Now we can wake some process.*/
    BGRT_PROC_FREE();                       /*May stop caller process.*/

    return ret;
}
